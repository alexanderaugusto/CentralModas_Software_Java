package model;

/**
 *
 * @author Alexander
 */
public class Store {

    private String cnpj;
    private String name;
    private String city;
    private String uf;
    private String cep;
    private String neighborhood;
    private String street;
    private String number;

    public Store(String cnpj, String name, String city, String uf, String cep, String neighborhood, String street, String number) {
        this.cnpj = cnpj;
        this.name = name;
        this.city = city;
        this.uf = uf;
        this.cep = cep;
        this.neighborhood = neighborhood;
        this.street = street;
        this.number = number;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getNeighborhood() {
        return neighborhood;
    }

    public void setNeighborhood(String neighborhood) {
        this.neighborhood = neighborhood;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

}
